static uint8_t bitswap8(uint8_t v, int b7, int b6, int b5, int b4, int b3, int b2, int b1, int b0)
{
    return (uint8_t)((((v >> b7) & 1u) << 7) | (((v >> b6) & 1u) << 6) |
                     (((v >> b5) & 1u) << 5) | (((v >> b4) & 1u) << 4) |
                     (((v >> b3) & 1u) << 3) | (((v >> b2) & 1u) << 2) |
                     (((v >> b1) & 1u) << 1) | (((v >> b0) & 1u) << 0));
}

static int bitswap6_addr(uint32_t a)
{
    return (int)((((a >> 14) & 1u) << 5) | (((a >> 12) & 1u) << 4) |
                 (((a >> 9) & 1u) << 3) | (((a >> 6) & 1u) << 2) |
                 (((a >> 3) & 1u) << 1) | ((a >> 0) & 1u));
}

static void decode_segacrpt(uint8_t *data, uint8_t *opcodes, uint32_t size, const uint8_t convtable[32][4])
{
    uint32_t a;
    if (!data || !opcodes) return;
    for (a = 0; a < size; a++)
    {
        uint8_t src = data[a];
        int row = (int)((a & 1u) | (((a >> 4) & 1u) << 1) | (((a >> 8) & 1u) << 2) | (((a >> 12) & 1u) << 3));
        int col = (int)(((src >> 3) & 1u) | (((src >> 5) & 1u) << 1));
        int xorval = 0;
        if (src & 0x80u)
        {
            col = 3 - col;
            xorval = 0xa8;
        }
        opcodes[a] = (uint8_t)((src & (uint8_t)~0xa8u) | (uint8_t)(convtable[2 * row][col] ^ xorval));
        data[a]    = (uint8_t)((src & (uint8_t)~0xa8u) | (uint8_t)(convtable[2 * row + 1][col] ^ xorval));
    }
}

static void decode_segacrp2(uint8_t *rom, uint8_t *decrypted, const uint8_t xor_table[128], const int swap_table[128])
{
    static const uint8_t swaptable[24][4] = {
        { 6,4,2,0 }, { 4,6,2,0 }, { 2,4,6,0 }, { 0,4,2,6 },
        { 6,2,4,0 }, { 6,0,2,4 }, { 6,4,0,2 }, { 2,6,4,0 },
        { 4,2,6,0 }, { 4,6,0,2 }, { 6,0,4,2 }, { 0,6,4,2 },
        { 4,0,6,2 }, { 0,4,6,2 }, { 6,2,0,4 }, { 2,6,0,4 },
        { 0,6,2,4 }, { 2,0,6,4 }, { 0,2,6,4 }, { 4,2,0,6 },
        { 2,4,0,6 }, { 4,0,2,6 }, { 2,0,4,6 }, { 0,2,4,6 } };
    uint32_t a;
    if (!rom || !decrypted) return;
    for (a = 0; a < 0x8000u; a++)
    {
        const uint8_t src = rom[a];
        const int row = bitswap6_addr(a);
        const uint8_t *tbl = swaptable[swap_table[2 * row]];
        decrypted[a] = bitswap8(src, 7, tbl[0], 5, tbl[1], 3, tbl[2], 1, tbl[3]) ^ xor_table[2 * row];
        tbl = swaptable[swap_table[2 * row + 1]];
        rom[a] = bitswap8(src, 7, tbl[0], 5, tbl[1], 3, tbl[2], 1, tbl[3]) ^ xor_table[2 * row + 1];
    }
}

